    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
      
        set.setText("");
        ProfileDao ad = new ProfileDaoImpl();
        Profile a = new Profile();

        
        a.setId(txtId.getText());
        a.setName(txtName.getText());
        a.setAddress(txtAddress.getText());
        a.setPost(txtPost.getSelectedItem().toString());
        a.setEmail(txtEmail.getText());
            String d=user.getText();
           String b=pass.getText();
 
            // TODO add your handling code here:
          
        DefaultTableModel dtm =(DefaultTableModel)ProfileTable.getModel();
       
       if(!txtId.getText().trim().equals("") && !txtName.getText().trim().equals("") && !txtAddress.getText().trim().equals("") && !txtPost.getSelectedItem().toString().equals("")&& !txtEmail.getText().trim().equals("")){
          JOptionPane.showMessageDialog(null, "Account Creation Successfull.");
           try {
            ad.submitProfile(a);
            dtm.addRow(new Object[]{txtId.getText(),txtName.getText(), txtAddress.getText(), txtPost.getSelectedItem(),txtEmail.getText()});
            BufferedWriter bw = new BufferedWriter(new FileWriter("Users.txt", true));
            bw.write(d + "," + b );
            bw.flush();
            bw.newLine();
            bw.close();
           } catch (IOException ex) {
            Logger.getLogger(AdminProfiles.class.getName()).log(Level.SEVERE, null, ex);
        } 
       }else{
        set.setText("Sorry! Please fill all the blank. Thank You!!!!");
       } 

    }//GEN-LAST:event_btnSubmitActionPerformed


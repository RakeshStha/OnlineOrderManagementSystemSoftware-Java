    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel model =(DefaultTableModel)ProfileTable.getModel();
           if(ProfileTable.getSelectedRow() == -1)
       {
           if(ProfileTable.getRowCount() == 0)
           {
               set.setText("Table is Empty");
           }else{
               set.setText("You must Select a Name of Student");
           }
       }else{
            try {
                model.removeRow(ProfileTable.getSelectedRow());
                ProfileDaoImpl p = new ProfileDaoImpl();
                p.deleteProfile(txtId.getText());
                p.deleteProfile(txtName.getText());
                p.deleteProfile(txtAddress.getText());
                p.deleteProfile(txtPost.getSelectedItem().toString());
                p.deleteProfile(txtEmail.getText());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Profile deletion succesfully");
                Logger.getLogger(AdminProfiles.class.getName()).log(Level.SEVERE, null, ex);
            }
       }         // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       
       DefaultTableModel tab =(DefaultTableModel)ProfileTable.getModel();
       if(ProfileTable.getSelectedRow() == -1)
       {
           if(ProfileTable.getRowCount() == 0)
           {
               set.setText("Table is Empty");
           }else{
               set.setText("You must Select a Manager");
           }
       }else{
           tab.setValueAt(txtId.getText(), ProfileTable.getSelectedRow(), 0);
           tab.setValueAt(txtName.getText(), ProfileTable.getSelectedRow(), 1);
           tab.setValueAt(txtAddress.getText(), ProfileTable.getSelectedRow(), 2);
           tab.setValueAt(txtPost.getSelectedItem().toString(), ProfileTable.getSelectedRow(), 3);
           tab.setValueAt(txtEmail.getText(), ProfileTable.getSelectedRow(), 4);
           
           
           Profile a = new Profile();
           a.Update(txtId.getText(),txtName.getText(),txtAddress.getText(), txtPost.getSelectedItem().toString(), txtEmail.getText());  
            JOptionPane.showMessageDialog(null, "Profile update succesfully");        
       }       
    }//GEN-LAST:event_btnUpdateActionPerformed

